package com.demo;

public class DeclarationVariable {

	public static void main(String[] args) {
		int number1 = 20;
		int number2 = 10;
		int addresult;
		int subresult;
		
		addresult = number1 + number2;
		
		subresult = number1 - number2;
		
		System.out.println("sum of two numbers " + addresult + "sub of two numbers \n" +subresult+"\n");
		System.out.println("sub of two numbers" + subresult);
	}

}
